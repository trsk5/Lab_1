public class ArithmeticOperations {
    public static void main(String[] args) {
        int var1 = 10;
        int var2 = 20;
        int var3 = 30;
        int var4 = 40;
        int var5 = 50;
        int var6 = 60;
        int var7 = 70;
        int var8 = 80;
        int var9 = 90;
        int var10 = 100;
        int sum = var1 + var2 + var3 + var4 + var5 + var6 + var7 + var8 + var9 + var10;
        System.out.println("Сума: " + sum);

        int difference = var1 - var2 - var3 - var4 - var5 - var6 - var7 - var8 - var9 - var10;
        System.out.println("Різниця: " + difference);


        int product = var1 * var2 * var3 * var4 * var5 * var6 * var7 * var8 * var9 * var10;
        System.out.println("Добуток: " + product);


        int division = var10 / var1;
        System.out.println("Ділення: " + division);
    }
}
